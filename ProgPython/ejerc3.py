# Luego realizar el mismo programa pero utilizando while


sueldos = [20100,2500,18250,1000]

print("Recorre array con for")

for s in sueldos:
    print(s)

print("Recorre array con while")

cont = 0
while cont < len(sueldos):
    print(sueldos[cont])
    cont=cont+1






